# กลุ่ม ทำซ้ำ (ทำงานเดิมๆ ซ้ำๆ) ->>> while, for

# while loop
i = 1
while i <= 5 :
    print(f'{i} SAU')
    i += 1

print('------------------------')

# for-in 
for x in range(5) :
    print(f'{x + 1} SAU')

print('------------------------')
# for-in 
for y in range(1, 6) :
    print(f'{y} SAU')

print('------------------------')
