print(10 // 7) #ส่วน 1
print(10 ** 7) #ยกกําลัง 10000000

# comparison operator
# == != > < >= <=

# logical operator
# and, or, not

# ***** สิ่งที่ต้องระวัง คือ operator ทุกตัวมีความสำคัญในการทำงานมากกว่าน้อยต่างกัน
